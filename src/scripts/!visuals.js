setup.logNote = function (title, mainText) {
	var div = document.createElement("div");
	//const noteID = "#" + setup.random(100);
	const noteID = "#" + logReel;
	div.innerHTML = "<div class='log-inner'><div class='log-title'>" + title + "</div><br>" + mainText + "<br></div>";
	div.setAttribute('class', 'log-update');
	div.setAttribute('id', logReel);
	logReel += 1;
	document.getElementById("notification-bar").appendChild(div);
	//insertBefore("#story").addClass("log-update");
	setTimeout(function(){
		$(noteID).remove();
		logReel -= 1;
	}, 12000);
	$(noteID).click(function(){
		$(noteID).remove();
	});
}

